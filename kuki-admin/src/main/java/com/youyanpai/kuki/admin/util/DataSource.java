package com.youyanpai.kuki.admin.util;

/**
 * 数据源
 * @author wangxd
 */
public class DataSource {
	
	/**
	 * 返回状态
	 */
	private String status;
	
	/**
	 * 返回信息
	 */
	private String msg;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
